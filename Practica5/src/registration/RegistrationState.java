package registration;

/**
 * Una enumeracion de los estados de una registracion
 * 
 * @author eps
 */
public enum RegistrationState{
	/**
	 * Estado STARTED
	 */
	STARTED, 
	/**
	 * Estado FAILED
	 */
	FILLED, 
	/**
	 * Estado VALIDATED
	 */
	VALIDATED, 
	/**
	 * Estado PAYED
	 */
	PAYED, 
	/**
	 * Estado FINISHED
	 */
	FINISHED, 
	/**
	 * Estado REJECTED
	 */
	REJECTED;
}
